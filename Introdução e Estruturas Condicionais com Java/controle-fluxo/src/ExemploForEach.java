public class ExemploForEach {
    public static void main(String[] args) {
        String alunos2 [] =  {"FELIPE","JONAS","JULIA","MARCOS"};
        
            //Forma abreviada
        for(String alunos : alunos2) {
          System.out.println(alunos);
        }
    
    }
    }